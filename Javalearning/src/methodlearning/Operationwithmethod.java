package methodlearning;

public class Operationwithmethod {
	
	public void addwarg(int a , int b) {
		System.out.println(a+b);
		
	}
	
	public int addwret(int a, int b) {
		return a+b;
	}
	
	public void add3warg(int a, int b, int c) {
		System.out.println(a+b+c);
	}
	
	public int add3withret(int a, int b, int c) {
		return a+b+c;
	}
	
	public void divwarg(int a, int b) {
		System.out.println(a/b);
	}
	
	public int divwret(int a, int b) {
		return a/b;
	}
	
	public void div3warg(int a, int b, int c) {
		System.out.println(a/b/c);
	}
	
	public int div3wret(int a, int b, int c) {
		return a/b/c;
	}
	
	public void subwarg(int a, int b) {
		System.out.println(a-b);
		
		
	}
	public int subwret(int a , int b) {
		return a-b;
		
	}
	
	public void sub3wreg(int a, int b, int c) {
		System.out.println(a-b-c);
	}
	
	public int sub3wret(int a, int b, int c) {
		return a-b-c;
	
	}	


}
